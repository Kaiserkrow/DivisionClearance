function districtFilter() {
  const schoolDropdown = document.getElementById("school");
  schoolDropdown.innerHTML = "";
  const districtDropdown = document.getElementById("district");
  //clears the dropdown
  districtDropdown.innerHTML = "";
  const choice = document.querySelector(
    "input[type='radio'][name=elem-or-hs]:checked"
  ).value;
  if (choice == "elem") {
    for (let i = 0; i < districtElem.length; i++) {
      districtDropdown.innerHTML += `<option data-elem="districtElem${
        i + 1
      }" value= "${districtElem[i].district}">${
        districtElem[i].district
      }</option>`;
    }
  } else {
    for (let i = 0; i < districtHS.length; i++) {
      districtDropdown.innerHTML += `<option value= "${
        districtHS[i].district
      }" data-HS="districtHS${i + 1}">${districtHS[i].district}</option>`;
    }
  }
}

function schoolFilter() {
  const schoolDropdown = document.getElementById("school");
  schoolDropdown.innerHTML = "";
  const elemHSChoice = document.querySelector(
    "input[type='radio'][name=elem-or-hs]:checked"
  ).value;
  const districtChoice = document.getElementById("district").value;
  if (elemHSChoice == "elem") {
    for (let i = 0; i < districtElem.length; i++) {
      if (districtChoice == districtElem[i].district) {
        for (let j = 0; j < districtElem[i].schools.length; j++) {
          schoolDropdown.innerHTML += `<option data-elem-schools="${districtElem[i].schools[j]}" value= "${districtElem[i].schools[j]}">${districtElem[i].schools[j]}</option>`;
        }
      }
    }
  } else {
    for (let i = 0; i < districtHS.length; i++) {
      if (districtChoice == districtHS[i].district) {
        for (let j = 0; j < districtHS[i].schools.length; j++) {
          schoolDropdown.innerHTML += `<option data-elem-schools="${districtHS[i].schools[j]}" value= "${districtHS[i].schools[j]}">${districtHS[i].schools[j]}</option>`;
        }
      }
    }
  }
}
